# Fahrenheit to Celsius:
# ----------------------
# Write the necessary code to convert a degree in Fahrenheit
# to Celsius and print it to the console. Use variable names!

# Define the Fahrenheit temperature
fahrenheit = 98.6

# Convert to Celsius using the formula
celsius = (5/9) * (fahrenheit - 32)

# Print the result
print(f"{fahrenheit} degrees Fahrenheit is equal to {celsius:.2f} degrees Celsius.")