# Use the interpreter to calculate how many seconds are in a year.
# Then write the code in this script file below this line.

# Define the numbers
seconds_in_a_minute = 60
minutes_in_a_hour = 60
hours_in_a_day = 24
days_in_a_year = 365

# calculate the total number of seconds in a year
seconds_in_a_year = seconds_in_a_minute * minutes_in_a_hour * hours_in_a_day * days_in_a_year

# Print the result
print("There are", seconds_in_a_year, "seconds in the year.")
